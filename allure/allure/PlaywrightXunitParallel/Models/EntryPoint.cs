namespace Gucu112.CSharp.Automation.PlaywrightXunitParallel.Models;

/// <summary>
/// Represents the entry point for an application.
/// </summary>
public class EntryPoint
{
    /// <summary>
    /// Gets or sets the name of the entry point.
    /// </summary>
    public string Name { get; set; } = null!;

    /// <summary>
    /// Gets or sets the URL address of the entry point.
    /// </summary>
    public string Url { get; set; } = null!;
}
